import pandas as pd
import streamlit as st
import pickle
from PIL import Image
import numpy as np
df = pd.read_csv('data.csv')

df.head()

df.describe()

ada = pickle.load(open('model.pkl', 'rb'))
st.title('Disorder Predictor')
temp = st.slider('Temperature', 0.0, 43.0, 36.6)
wbc = st.slider('WBC Count', 0.0, 16.0, 7.5)
heart_rate = st.slider('Heart Rate', 0, 50, 140)
age = st.slider('Age', 0, 100, 40)

input_data  = np.array([temp,wbc,heart_rate,age])
if st.button('Predict'):
  rf_res = ada.predict(input_data)[0]
  st.subheader('Results')
  st.write(f"Prediction: {'İll' if rf_res == 1 else 'Not İll'}")

st.subheader('Feature importances: ')
st.image('feature_importance.png')
